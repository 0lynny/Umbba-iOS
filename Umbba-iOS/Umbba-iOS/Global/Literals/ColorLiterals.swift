import UIKit

extension UIColor {
    static var Primary400: UIColor {
        return UIColor(hex: "#F1BD9B")
    }
    
    static var Primary500: UIColor {
        return UIColor(hex: "#FE99F7A")
    }
    
    static var Primary600: UIColor {
        return UIColor(hex: "#C87759")
    }
    
    static var Gray300: UIColor {
        return UIColor(hex: "#F7F2F0")
    }
    
    static var Gray400: UIColor {
        return UIColor(hex: "#F0EBE8")
    }
    
    static var Gray500: UIColor {
        return UIColor(hex: "#E7E0DC")
    }

    static var Error: UIColor {
        return UIColor(hex: "#FF3A1F")
    }
    
    static var Success: UIColor {
        return UIColor(hex: "#419277")
    }
    
    static var UmbbaBlack: UIColor {
        return UIColor(hex: "#32211B")
    }
    
    static var UmbbaWhite: UIColor {
        return UIColor(hex: "#FCFAF8")
    }
}
